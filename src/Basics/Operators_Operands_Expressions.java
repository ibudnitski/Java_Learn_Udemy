package Basics;

public class Operators_Operands_Expressions {
    public static void main(String[] args) {
        int result = 1 + 2;
        System.out.println("result = " + result);
        char firstChar = 'A';
        char secondChar = 'B';
        System.out.println(firstChar + secondChar);
        System.out.println("" + firstChar + secondChar);

        // Incrementation

        int result1 = 1;
        result1++;
        System.out.println("result1 = " + result1);
        result1--;
        System.out.println("result1 = " + result1);
    }
}
