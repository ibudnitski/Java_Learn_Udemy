package Basics;

public class variables {
    public static void main(String[] args) {
//        Primitive types
        boolean trueBoolean = true;
        boolean falseBoolean = false;
        char charType = 'a';
        System.out.println(Byte.MAX_VALUE + " byte " + Byte.MAX_VALUE);
        System.out.println(Short.MAX_VALUE + " short " + Short.MAX_VALUE);
        System.out.println(Integer.MAX_VALUE + " Integer " + Integer.MAX_VALUE);
        System.out.println(Long.MAX_VALUE + " Long " + Long.MAX_VALUE);
        System.out.println(Float.MAX_VALUE + " Float " + Float.MAX_VALUE);
        System.out.println(Double.MAX_VALUE + " Double " + Double.MAX_VALUE);
        System.out.println(trueBoolean + " Boolean TRUE / FALSE " + falseBoolean);
        System.out.println("charType = " + charType);
    }
}
